# PyTorch 1.7 has SiLU, but we support PyTorch 1.5.
class SiLU(nn.Module):
    def forward(self, x):
        return x * th.sigmoid(x)


class LayerNorm32(nn.LayerNorm):
    def forward(self, x):
        return super().forward(x.float()).type(x.dtype)


class GroupNorm32(nn.GroupNorm):
    def forward(self, x):
        return super().forward(x.float()).type(x.dtype)


class ScaledSoftmax32(nn.Module):
    def __init__(self, scale):
        super().__init__()
        self.scale = scale

    def forward(self, x, mask):
        return th.softmax(self.scale * x.float(), dim=-1).to(x.dtype)


class FusedSoftmaxMaybeApex(nn.Module):
    def __init__(self, scale):
        super().__init__()
        if os.environ.get("APEX", ""):
            from apex.transformer import AttnMaskType
            from apex.transformer.functional import FusedScaleMaskSoftmax

            def attention_mask_func(attention_scores, attention_mask):
                return attention_scores.masked_fill(attention_mask, -10000.0)

            self.fn = FusedScaleMaskSoftmax(
                input_in_fp16=True,
                input_in_bf16=False,
                mask_func=attention_mask_func,
                scale=scale,
                softmax_in_fp32=True,
                attn_mask_type=AttnMaskType.padding,
                scaled_masked_softmax_fusion=True,
            )
        else:
            self.fn = ScaledSoftmax32(scale)

    def forward(self, x, mask=None):
        shape = x.shape
        if len(x.shape) == 3:
            x = x.unsqueeze(1)
            # reshape after in the unlikely case of h=1

        mask = th.zeros(x.shape[0], 1, x.shape[2], x.shape[3], device=x.device).bool()
        return self.fn(x, mask=mask).reshape(*shape).type(x.dtype)


